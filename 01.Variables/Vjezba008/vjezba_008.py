prvi_broj = int(input("Upišite prvi broj: "))
drugi_broj = int(input("Upišite drugi broj: "))

zbroj = prvi_broj + drugi_broj
razlika = prvi_broj - drugi_broj
umnozak = prvi_broj * drugi_broj
kolicnik = prvi_broj / drugi_broj
potencija = prvi_broj ** drugi_broj
modulo = prvi_broj % drugi_broj

print(f"{prvi_broj} + {drugi_broj} = {zbroj}")
print(f"{prvi_broj} - {drugi_broj} = {razlika}")
print(f"{prvi_broj} * {drugi_broj} = {umnozak}")
print(f"{prvi_broj} / {drugi_broj} = {kolicnik}")
print(f"{prvi_broj} ** {drugi_broj} = {potencija}")
print(f"{prvi_broj} % {drugi_broj} = {modulo}")